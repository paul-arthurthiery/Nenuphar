package com.nenuphar.nenufar.Models;

public class Group
{
    private int id;
    private String name;
    private int Size;

    public Group()
    {

    }

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getSize()
    {
        return Size;
    }

    public void setSize(int size)
    {
        this.Size = size;
    }
}
