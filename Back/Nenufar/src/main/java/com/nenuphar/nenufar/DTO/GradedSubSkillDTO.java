package com.nenuphar.nenufar.DTO;

public class GradedSubSkillDTO
{

    private int level;
    private String subSkillName, courseName, name, last_name;


    public int getLevel() { return level; }

    public void setLevel(int level) { this.level = level; }

    public String getSubSkillName() { return subSkillName; }

    public void setSubSkillName(String subSkillName) { this.subSkillName = subSkillName; }

    public String getCourseName() { return courseName; }

    public void setCourseName(String courseName) { this.courseName = courseName; }

    public String getName() { return name; }

    public void setName(String name) { this.name = name; }

    public String getLast_name() { return last_name; }

    public void setLast_name(String last_name) { this.last_name = last_name; }
}
