package com.nenuphar.nenufar.Models;

public class Equipe
{
    private int id;
    private int taille;
    private String name;

    public Equipe()
    {

    }

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public int getTaille()
    {
        return taille;
    }

    public void setTaille(int taille)
    {
        this.taille = taille;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
